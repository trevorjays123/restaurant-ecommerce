// Helper function to get items by cuisine
export const getItemsByCuisine = (cuisine: string): MenuItem[] => {
  return menuItems.filter(item => item.cuisine === cuisine);
};

// Helper function to get items by category
export const getItemsByCategory = (category: string): MenuItem[] => {
  return menuItems.filter(item => item.category.toLowerCase() === category.toLowerCase());
};

// Get all unique categories
export const getAllCategories = (): string[] => {
  const categories = new Set(menuItems.map(item => item.category));
  return Array.from(categories);
};

// Get cuisine-specific categories
export const getCuisineCategories = (cuisine: string): string[] => {
  const categories = new Set(
    menuItems
      .filter(item => item.cuisine === cuisine)
      .map(item => item.category)
  );
  return Array.from(categories);
};
